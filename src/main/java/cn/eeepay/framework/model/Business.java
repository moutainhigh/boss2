package cn.eeepay.framework.model;

public class Business {
    private String oldBpId;
    private String newBpId;
    private String oldBpName;
    private String newBpName;

    public String getOldBpId() {
        return oldBpId;
    }

    public void setOldBpId(String oldBpId) {
        this.oldBpId = oldBpId;
    }

    public String getNewBpId() {
        return newBpId;
    }

    public void setNewBpId(String newBpId) {
        this.newBpId = newBpId;
    }

    public String getOldBpName() {
        return oldBpName;
    }

    public void setOldBpName(String oldBpName) {
        this.oldBpName = oldBpName;
    }

    public String getNewBpName() {
        return newBpName;
    }

    public void setNewBpName(String newBpName) {
        this.newBpName = newBpName;
    }
}
