package cn.eeepay.framework.model.three;

/**
 * 
 * @author qiujian
 *
 */
public class ThreeSumVo {

	private String tradeSum;
	private String merSum;
	private String activeSum;
	private String threeIncomeSum;
	// 入账总额
	private String recordedSum;

	public String getTradeSum() {
		return tradeSum;
	}

	public void setTradeSum(String tradeSum) {
		this.tradeSum = tradeSum;
	}

	public String getMerSum() {
		return merSum;
	}

	public void setMerSum(String merSum) {
		this.merSum = merSum;
	}

	public String getActiveSum() {
		return activeSum;
	}

	public void setActiveSum(String activeSum) {
		this.activeSum = activeSum;
	}

	public String getThreeIncomeSum() {
		return threeIncomeSum;
	}

	public void setThreeIncomeSum(String threeIncomeSum) {
		this.threeIncomeSum = threeIncomeSum;
	}

	public String getRecordedSum() {
		return recordedSum;
	}

	public void setRecordedSum(String recordedSum) {
		this.recordedSum = recordedSum;
	}

}
