package cn.eeepay.framework.model.three;

public class SumDo {

	private String transAmount;
	private String agentNo;
	private String teamId;
	private String merSum;
	private String activateSum;
	private String machinesStock;
	private String unusedMachines;
	private String expiredNotActivated;

	public String getAgentNo() {
		return agentNo;
	}

	public void setAgentNo(String agentNo) {
		this.agentNo = agentNo;
	}

	public String getTeamId() {
		return teamId;
	}

	public void setTeamId(String teamId) {
		this.teamId = teamId;
	}

	public String getMerSum() {
		return merSum;
	}

	public void setMerSum(String merSum) {
		this.merSum = merSum;
	}

	public String getTransAmount() {
		return transAmount;
	}

	public void setTransAmount(String transAmount) {
		this.transAmount = transAmount;
	}

	public String getActivateSum() {
		return activateSum;
	}

	public void setActivateSum(String activateSum) {
		this.activateSum = activateSum;
	}

	public String getMachinesStock() {
		return machinesStock;
	}

	public void setMachinesStock(String machinesStock) {
		this.machinesStock = machinesStock;
	}

	public String getUnusedMachines() {
		return unusedMachines;
	}

	public void setUnusedMachines(String unusedMachines) {
		this.unusedMachines = unusedMachines;
	}

	public String getExpiredNotActivated() {
		return expiredNotActivated;
	}

	public void setExpiredNotActivated(String expiredNotActivated) {
		this.expiredNotActivated = expiredNotActivated;
	}

}
